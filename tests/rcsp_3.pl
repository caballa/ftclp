entry_goal(Src, Dest, Rf, Cf, C, R, Path):- 
	clp_meta([C0 .=. 0, R0  .=. 0]), 
	r(Src, Dest, [], Rf, Cf, C0, R0, C, R, Path),
	clp_meta([R .=<. Rf, C .=<. Cf]).

:- tabled(r(_,_,_,num,num,num,num,num,num,_)).
:- discriminants(r(d,d,nd,nd,nd,d,d,d,d,nd)).
r(X,Y,V,_Rf,_Cf,C0,R0,C2,R2,[(X-Y)]) :-
	edge(X,Y,C1,R1),
	clp_meta([
		     C2 .=. C0 + C1, R2 .=. R0 + R1
		 ]),
	\+(member(X,V)).
r(X,Y,V,Rf, Cf,C0,R0,C3,R3,[(X-Z)|Path]) :-
	edge(X,Z,C1,R1),
	\+(member(X,V)),
	clp_meta([
		     C2 .=. C0 + C1, R2 .=. R0 + R1,
		     R2 .=<. Rf,
		     C2 .=<. Cf
		 ]),
	r(Z,Y,[X|V],Rf,Cf,C2,R2,C3,R3,Path).

:- no_cache(member(_,_)).
member(X,[X|_]).
member(X,[_|Xs]):- member(X,Xs).

